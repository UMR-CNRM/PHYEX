SUBROUTINE XRD_COUNTLINES( NLINES, F, ERR )
IMPLICIT NONE
INTEGER(KIND=JPIM), INTENT(OUT) :: NLINES
CHARACTER*(*), INTENT(IN) :: F
INTEGER(KIND=JPIM), INTENT(OUT) :: ERR
CHARACTER*32 :: STR

NLINES = 0
OPEN( 77, FILE = F, ERR = 888 )

DO
  READ( 77, *, ERR = 888, END = 777 ) STR
  NLINES = NLINES + 1
ENDDO

777 CONTINUE

CLOSE( 77 )

RETURN
888 CONTINUE
  ERR = 1
END SUBROUTINE

FUNCTION XRD_COUNTWORDS( S )
  IMPLICIT NONE
  INTEGER(KIND=JPIM) :: XRD_COUNTWORDS
  CHARACTER(LEN=*), INTENT(IN) :: S
  INTEGER(KIND=JPIM) :: N, I, L
  LOGICAL(KIND=JPLM) :: IN
  N = 0_JPIM
  IN = .FALSE.
  L = LEN( TRIM( S ) )
  DO I = 1, L
    IF( S(I:I) .EQ. ' ' ) THEN
      IN = .FALSE.
    ELSE IF( .NOT. IN ) THEN
      N = N + 1
      IN = .TRUE.
    ENDIF
  ENDDO
  XRD_COUNTWORDS = N
END FUNCTION

